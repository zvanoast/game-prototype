// ─── Process vehicles ───────────────────────────────────────────────

const VEHICLE_IDS = ["office_chair", "red_wagon", "golf_cart", "jet_ski", "fork_lift"];
const VEHICLE_DIRS = ["down", "up", "right", "left"] as const;

async function processVehicles(): Promise<boolean> {
  let processed = 0;
  for (const vehicleId of VEHICLE_IDS) {
    const rawPath = path.join(ASSETS_DIR, "vehicles", `vehicle_${vehicleId}_raw.png`);
    if (!fs.existsSync(rawPath)) continue;

    const img = await loadImage(rawPath);
    const cellSize = Math.floor(img.width / 4);
    const targetSize = 40;

    for (let i = 0; i < VEHICLE_DIRS.length; i++) {
      const sx = i * cellSize;

      const canvas = createCanvas(targetSize, targetSize);
      const ctx = canvas.getContext("2d");
      ctx.imageSmoothingEnabled = false;
      ctx.drawImage(img, sx, 0, cellSize, cellSize, 0, 0, targetSize, targetSize);

      const outPath = path.join(ASSETS_DIR, "vehicles", `vehicle_${vehicleId}_${VEHICLE_DIRS[i]}.png`);
      fs.writeFileSync(outPath, canvas.toBuffer("image/png"));
    }
    processed++;
  }

  if (processed > 0) {
    console.log(`  ✓ ${processed} vehicles split`);
  } else {
    console.log("  [skip] No vehicle raw images found");
  }
  return processed > 0;
}

// ─── Process environment tiles ──────────────────────────────────────

const ENV_TILES = [
  "floor", "floor_accent", "wall", "wall_top", "wall_front",
  "locker_closed", "locker_open", "shadow",
];

async function processEnvironment(): Promise<boolean> {
  const rawPath = path.join(ASSETS_DIR, "environment", "tiles_raw.png");
  if (!fs.existsSync(rawPath)) {
    console.log("  [skip] tiles_raw.png not found");
    return false;
  }

  console.log("  Processing environment tiles...");
  const img = await loadImage(rawPath);

  const cols = ENV_TILES.length;
  const cellSize = Math.floor(img.width / cols);
  const targetSize = 32;

  for (let i = 0; i < ENV_TILES.length; i++) {
    const sx = i * cellSize;
    const h = ENV_TILES[i] === "shadow" ? 16 : targetSize;

    const canvas = createCanvas(targetSize, h);
    const ctx = canvas.getContext("2d");
    ctx.imageSmoothingEnabled = false;
    ctx.drawImage(img, sx, 0, cellSize, cellSize, 0, 0, targetSize, h);

    const outPath = path.join(ASSETS_DIR, "environment", `env_${ENV_TILES[i]}.png`);
    fs.writeFileSync(outPath, canvas.toBuffer("image/png"));
  }

  console.log(`  ✓ ${ENV_TILES.length} environment tiles split`);
  return true;
}

// ─── Main ───────────────────────────────────────────────────────────

async function main() {
  console.log("=== Sprite Post-Processing ===\n");
  console.log(`Assets directory: ${ASSETS_DIR}\n`);

  // Characters
  console.log("Characters:");
  let charCount = 0;
  for (let i = 0; i < 9; i++) {
    if (await processCharacter(i)) charCount++;
  }
  console.log(`  ${charCount}/9 characters processed\n`);

  // Items
  console.log("Items:");
  await processPickups();
  await processProjectiles();
  console.log();

  // Vehicles
  console.log("Vehicles:");
  await processVehicles();
  console.log();

  // Environment
  console.log("Environment:");
  await processEnvironment();
  console.log();

  console.log("=== Done ===");
  console.log("Run `npm run pack-atlas` next to build Phaser JSON atlases.");
}

main().catch((err) => {
  console.error("Error:", err);
  process.exit(1);
});
